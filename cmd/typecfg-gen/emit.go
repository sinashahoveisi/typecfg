func emit(opts Options, fields []fieldInfo) ([]byte, error) {
	self := opts.PackageName == "typecfg"
	pfx := "typecfg."
	if self {
		pfx = ""
	}

	var b bytes.Buffer
	b.WriteString("// Code generated by typecfg-gen. DO NOT EDIT.\n\n")
	fmt.Fprintf(&b, "package %s\n\n", opts.PackageName)

	imports := collectImports(self)
	if len(imports) > 0 {
		b.WriteString("import (\n")
		for _, im := range imports {
			fmt.Fprintf(&b, "\t%q\n", im)
		}
		b.WriteString(")\n\n")
	}

	binder := opts.TypeName + "Binder"
	fmt.Fprintf(&b, "// %s implements %sGeneratedBinder[%s].\n", binder, pfx, opts.TypeName)
	fmt.Fprintf(&b, "type %s struct{}\n\n", binder)

	fmt.Fprintf(&b, "func (%s) BindGenerated(raw map[string]any) (*%s, map[string]struct{}, []*%sFieldError) {\n", binder, opts.TypeName, pfx)
	fmt.Fprintf(&b, "\tcfg := &%s{}\n", opts.TypeName)
	b.WriteString("\tsetFields := map[string]struct{}{}\n")
	fmt.Fprintf(&b, "\tvar errs []*%sFieldError\n", pfx)
	emitBindFields(&b, fields, "raw", pfx)
	b.WriteString("\treturn cfg, setFields, errs\n}\n\n")

	fmt.Fprintf(&b, "func (%s) ValidateGenerated(cfg *%s, setFields map[string]struct{}, raw map[string]any) []*%sFieldError {\n", binder, opts.TypeName, pfx)
	fmt.Fprintf(&b, "\tvar errs []*%sFieldError\n", pfx)
	emitValidateFields(&b, fields, "raw", "cfg", pfx)
	fmt.Fprintf(&b, "\tif v, ok := any(cfg).(%sValidator); ok {\n", pfx)
	b.WriteString("\t\tif err := v.Validate(); err != nil {\n")
	fmt.Fprintf(&b, "\t\t\terrs = append(errs, &%sFieldError{Field: \"<struct>\", Tag: \"Validate()\", Reason: err.Error()})\n", pfx)
	b.WriteString("\t\t}\n")
	b.WriteString("\t}\n")
	b.WriteString("\treturn errs\n}\n")

	return b.Bytes(), nil
}

func collectImports(self bool) []string {
	var out []string
	if !self {
		out = append(out, "github.com/sinashahoveisi/typecfg")
	}
	return out
}

func emitBindFields(b *bytes.Buffer, fields []fieldInfo, rawExpr, pfx string) {
	for _, f := range fields {
		if f.Kind == kindStruct {
			fmt.Fprintf(b, "\t{\n")
			fmt.Fprintf(b, "\t\tnested, _ := %sLookup(%s, %q)\n", pfx, rawExpr, f.CfgKey)
			fmt.Fprintf(b, "\t\tvar nestedMap map[string]any\n")
			fmt.Fprintf(b, "\t\tif m, ok := nested.(map[string]any); ok {\n")
			fmt.Fprintf(b, "\t\t\tnestedMap = m\n")
			fmt.Fprintf(b, "\t\t}\n")
			emitBindFields(b, f.Nested, "nestedMap", pfx)
			fmt.Fprintf(b, "\t}\n")
			continue
		}
		emitBindScalar(b, f, rawExpr, pfx)
	}
}

func emitBindScalar(b *bytes.Buffer, f fieldInfo, rawExpr, pfx string) {
	secret := fmt.Sprintf("%v", f.Secret)
	fmt.Fprintf(b, "\tif v, ok := %sLookup(%s, %q); ok {\n", pfx, rawExpr, f.CfgKey)
	emitAssignFromRaw(b, f, "v", pfx, secret)
	if f.HasDefault {
		fmt.Fprintf(b, "\t} else {\n")
		emitAssignFromDefault(b, f, pfx, secret)
		fmt.Fprintf(b, "\t}\n")
	} else {
		fmt.Fprintf(b, "\t}\n")
	}
}

