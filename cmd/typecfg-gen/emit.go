func emitDefaultErrCheck(b *bytes.Buffer, f fieldInfo, pfx, secret, def, expr, dst string) {
	fmt.Fprintf(b, "\t\tif err != nil {\n")
	fmt.Fprintf(b, "\t\t\terrs = append(errs, %sNewDefaultError(%q, %q, %s, err))\n", pfx, f.FieldPath, def, secret)
	fmt.Fprintf(b, "\t\t} else {\n")
	fmt.Fprintf(b, "\t\t\t%s = %s\n", dst, expr)
	fmt.Fprintf(b, "\t\t\tsetFields[%q] = struct{}{}\n", f.FieldPath)
	fmt.Fprintf(b, "\t\t}\n")
}

func emitValidateFields(b *bytes.Buffer, fields []fieldInfo, rawExpr, cfgExpr, pfx string) {
	for _, f := range fields {
		if f.Kind == kindStruct && hasValidate(f.Nested) {
			fmt.Fprintf(b, "\t{\n")
			fmt.Fprintf(b, "\t\tnestedRaw := %sNestedRaw(%s, %q)\n", pfx, rawExpr, f.CfgKey)
			emitValidateFields(b, f.Nested, "nestedRaw", cfgExpr, pfx)
			fmt.Fprintf(b, "\t}\n")
		}
		if f.Validate == "" {
			continue
		}
		for _, rule := range strings.Split(f.Validate, ",") {
			rule = strings.TrimSpace(rule)
			if rule == "" {
				continue
			}
			name, arg, _ := strings.Cut(rule, "=")
			secret := fmt.Sprintf("%v", f.Secret)
			fmt.Fprintf(b, "\tif reason := %sCheckRule(%q, %q, %q, %q, %s.%s, setFields, %s, %s); reason != \"\" {\n",
				pfx, name, arg, f.FieldPath, f.CfgKey, cfgExpr, f.AccessPath, rawExpr, secret)
			fmt.Fprintf(b, "\t\terrs = append(errs, &%sFieldError{\n", pfx)
			fmt.Fprintf(b, "\t\t\tField: %q, Tag: %q,\n", f.FieldPath, name)
			fmt.Fprintf(b, "\t\t\tSources: %sFieldSources(%q, %q),\n", pfx, f.CfgKey, f.EnvTag)
			fmt.Fprintf(b, "\t\t\tReason: reason,\n")
			fmt.Fprintf(b, "\t\t})\n")
			fmt.Fprintf(b, "\t}\n")
		}
	}
}

func hasValidate(fields []fieldInfo) bool {
	for _, f := range fields {
		if f.Validate != "" {
			return true
		}
		if f.Kind == kindStruct && hasValidate(f.Nested) {
			return true
		}
	}
	return false
}
