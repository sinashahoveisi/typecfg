func emitBindErrCheck(b *bytes.Buffer, f fieldInfo, pfx, secret, expr, dst string) {
	fmt.Fprintf(b, "\t\tif err != nil {\n")
	fmt.Fprintf(b, "\t\t\terrs = append(errs, %sNewBindTypeError(%q, %q, %q, %s, err))\n", pfx, f.FieldPath, f.CfgKey, f.TypeName, secret)
	fmt.Fprintf(b, "\t\t} else {\n")
	fmt.Fprintf(b, "\t\t\t%s = %s\n", dst, expr)
	fmt.Fprintf(b, "\t\t\tsetFields[%q] = struct{}{}\n", f.FieldPath)
	fmt.Fprintf(b, "\t\t}\n")
}

